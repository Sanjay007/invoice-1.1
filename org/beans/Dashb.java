package org.beans;

public class Dashb {

	int invoicecount;
	int usercount;
	int productcount;
	int unbilledcount;
	int amountpaidcount;
	public int getInvoicecount() {
		return invoicecount;
	}
	public void setInvoicecount(int invoicecount) {
		this.invoicecount = invoicecount;
	}
	public int getUsercount() {
		return usercount;
	}
	public void setUsercount(int usercount) {
		this.usercount = usercount;
	}
	public int getProductcount() {
		return productcount;
	}
	public void setProductcount(int productcount) {
		this.productcount = productcount;
	}
	public int getUnbilledcount() {
		return unbilledcount;
	}
	public void setUnbilledcount(int unbilledcount) {
		this.unbilledcount = unbilledcount;
	}
	public int getAmountpaidcount() {
		return amountpaidcount;
	}
	public void setAmountpaidcount(int amountpaidcount) {
		this.amountpaidcount = amountpaidcount;
	}
	
}
